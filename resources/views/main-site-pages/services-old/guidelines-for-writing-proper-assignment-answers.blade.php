					<br>

					
<p>When the assignment is received, the assignment should not simply replicate the information from your textbooks. In that case, you should come up with your own ideas or views, supported by satisfactory number of evidences or examples. All the data which are presented should be collected during the research work while you are <b>preparing  your assignment.</b></p>

<h3>Tips to construct Structure of an Assignment</h3>
<ul><li> <b>Make an Outline: </b>Once you start to write your assignment,you should write an outline for the assignment which comprises the key points which will be discussed. There are various benefits of this step for instances saving of time, organising your ideas and this will also help you to divide up your word count between variousdivisions.</li>
 
<li> <b>Write a proper Introduction: </b>Writing a proper introduction is helpful to initiate your assignment with a clear vision. The introduction should comprise a brief background citing what the assignment will illustrate. The introductionshould be written in 1-2 paragraphs.</li></ul>

<p>There are many characteristic of an ideal completed assignment. The all characteristics share some common characteristics, which are described here:
<ol><li> Proper answers of the question asked in the <b>assignment topic</b></li>
<li> The assignment must be rational and coherent;</li>
<li> The writing/answers must follow the same manner, communication as well as idea and presentation.</li></ol></p>

<p>The guidelines which should be taken when preparing your assignment are:
<ol><li> First, Read the questions–if you have any problem in understanding these questions, please ask your tutor. </li>
<li> Analyseif there are any different sub questions or sub-topic?</li>
<li> Analyse the assessment criteria </li>
<li> Analysis the marking grid</li>
<li> After completing the step 1-4, Compose a <b>draft structure of an assignment. </b>The assignment should have key headings and all the key points that you want to embrace in each section. The draft should be written in a logical order. The draft should be like presenting your assignment in short story.</li>
<li> After completion of the draft; it should be checked by your tutor.</li>
<li> Collect all the resources like journals, books, list of websites which will be helpful in writing your assignment.</li>
<li> Start to write according to <b>required assignment format. </b></li>
<li> Complete the assignment as per marking grid/requirement.</li></ol>
</p>

<p>Make sure your assignment maintain these points as follows.
<li> <b>Usage of ‘I’ and ‘you’: </b>You should avoid using ‘I’ or ‘you’ in your <b>academic writing. </b> This is important represent a clear, unbiased picture. You should include your ideas indirectly.</li>
<li> <b>Steadiness of ideas: </b> Ensure that continuity of ideas both between paragraphs and within a paragraph should be maintained as this will enable the reader to follow your train of thought/ argument easily. </li>
<li> <b>Usage of examples: </b> If possible use some examples as well to support your opinion if any.</li>
<li> <b>Word Count: </b>Main the word count properly. You should divide the whole writing according to proper word count.</li>
<li> <b>Insertion of tables and figures: </b> Table and figures should be properly inserted in appropriate paragraph.</li>
<li> Insert bibliography, in texting, appendices etc. according to the requirement. </li>
<li> <b>Conclusion: </b>Proper conclusion should be included as this part conclude the main <b>finding of writing /outcomes of writing. </b></li></ol>
</p>

							
