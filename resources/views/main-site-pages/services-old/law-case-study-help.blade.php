<p>One of the core parts of the <b>case study analysis</b> is law case studies. Law case study helps the reader to learn about the law. If the individual learns about the <b>law case studies</b> or reads more and more law case studies sample, he can solve the conflict in the different situation. At the beginning of the course, the students will analyse the law cases differently than the professional lawyer. You can analyse a law case study simply as the lawyer by applying a simple approach. It will help you to do the legal analysis easier.</p>
<p>
According to the approach, you can divide the <b>law case study</b> into five steps. These are-
<ol><li> At first, you have to state the issue of the case study.</li> 
<li> You have to cite the case study. Based on the holding of the cases that you have the done the courses, describe the law that will be suitable for that situation. </li> 
<li> You have to apply the suitable law for the stated case study, to get an ironic outcome. </li> 
<li> Then you have to interpret the difference between the fact situation in the case that you have stated and the fact situation that you are analysing.</li> 
<li> At the last stage, the fundamental nature of the legal conflict will be analysed by you. You should also analyse what you feel about the case and the opinion of the public about the <b>legal case study. </b></li> </ol>

</p>	


<p>In a law school, if the teacher suggests the students to <b>write case study, </b> the students must follow a constructed structure at the time of writing a law case study. The may follow some <b>law case assignment</b> sample. In the law case study format, the first stage will be</p>
<p>Case Synopsis: It means what is the case study about or what is the fundamental topic of the legal case study. This will not be more than half page.</p>
<p>Objectives of law case study: you have to identify the goals for the stated case study that you are writing.</p>
<p>Case Analysis: After defining the goals you have to analyse the case or the issues of the case. In this stage, you describe the whole problem of the case study.</p>
<p>Methodology: You have to research the case and explain in detail the source of information.</p>
<p>Modification Notes: In this section, you will state the modified cases. The modification will take place to develop the conflicts or characters, to create a relevant content which the research has not revealed and to present confidentially.</p>
<p>You need to understand and read the court decision, to understand the law of the case study entirely. You can use the brief case study analysis to make the case writing more quickly. When you follow the brief method in the law case study writing, it will be a relatively standard procedure. Before writing a law case study, you have to read the opinion of the case study in details. You can prepare a proper case study if you have understood the case. You have to present a legal case study framework to state the case study methodically.
<ol><li> Citation: At first of a law case study, you have to do the citation with the proper date it was decided, the court that decided it and the name of the case study. In the citation, you can use <b>APA citation style</b> or <b>Harvard citation style. </b></li>
<li> Facts: This is the illustration part of the case study. In this section (1) the lower court’s decision, (2) the argument and the identity of defendant and plaintiff and (3) the reason for the lawsuit all of these will be described.</li>
<li> Issue: The issue of the legal case study will be displayed in this section. The crucial points of this section are the essential issue before the court and the form of the question that a client may face. </li>
<li> Decision: The decision is the answer to the court will be described in this section. The court will give the answer in ‘yes' or ‘no'. The court will give the answer depending on the issue of the case study. </li>
<li> Reason: The possible reason of the court's answer will be concise in this section. This section will also describe depending on which law, the court has arrived at this decision.
	</li></ol> </p>

	
