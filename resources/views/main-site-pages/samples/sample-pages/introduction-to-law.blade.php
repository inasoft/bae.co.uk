							Mediation

						</strong>
						<p>
						Mediation is an effective alternate dispute solution tool. In mediation a third party brings the parties to the dispute to the negotiating position so can resolve the dispute between the parties (Brenneur et al., 2010). The parties to the dispute through the involvement of third party can reach to an alternate solution for the dispute. 
  
						</p>

                        <strong>
                        	Conciliation
                        </strong>
						<p>
						Conciliation is dispute solution technique where the parties to the dispute appoint a person who acts as a conciliator. The conciliator meets the parties to the dispute separately and brings them to the solution of the dispute. Conciliators acts as mediator between the parties independently and encourage the parties to the dispute to reach alternative solution of dispute (Binder, 2010).


						</p>
						<strong>
							Advantage and Disadvantage of Mediation in the scenario

						</strong>


						<p>
						Mediation is that process of whereby the parties to the dispute reach to the solution by involvement of third party. The two companies can appoint an expert for resolving the matter. They can take the matter to the mediator and can resolve the dispute by proper mediation and negotiation (Brenneur et al., 2010). However the session of the mediator can be adjourned intentionally by one of the party that brings the late solution of dispute. 

						</p>

