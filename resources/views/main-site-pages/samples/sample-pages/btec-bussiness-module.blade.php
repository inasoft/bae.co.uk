						<p class="fs-18"><strong>1.3 Essential Content</strong></p>
						<strong>LO1 Explain the role of marketing and how it interrelates with other functional units of an organisation</strong>
						<p> Definitions and the marketing concept:</p>
						<ul>
							<li>
								<b>Definitions of marketing and the nature of marketing.</b>
								The development of the marketing concept, including current and future trends. How the external environment influences and impacts upon marketing activity.
							</li>
							<li>
								<b>The role of marketing:</b>
								The structure and operations of marketing departments.
                                Overview of marketing processes that include analysis, strategic planning and the marketing mix.
                                The different roles of marketing within both a B2C and B2B context.
							</li>
							<li>
								<b>The interrelationships of functional units:</b>
								Marketing as a business function.
                                The different roles of business units and the interrelationships between these functional units and marketing.
							</li>
						</ul>

						<hr class="divider-color" />
                        <br>
						<p class="fs-18"><strong>LO2 Compare ways in which organisations use elements of the marketing mix (7Ps) to achieve overall business objectives</strong></p>
						<strong>The 7Ps marketing mix:</strong>
						<ul>
							<li>
								<b>Product:</b> Differences between products and services, importance of brands, product development and product lifestyle.
							</li>
							<li>
								<b>Price: </b>Pricing context, pricing strategies and tactics.
							</li>
							<li>
								<b>Place:</b> Channel management, supply chain management and logistics.
							</li>
							<li>
								<b>Promotion:</b> Integrated communication mix and promotional tools.
							</li>
							<li>
								<b> People: </b>The different roles of ‘people’ in marketing, including customer interfacing and support personnel. The different skills, attitudes and behaviour of people delivering the product or service to customers.
							</li>
							<li>
								<b>Physical evidence:</b> The tangible aspects of service delivery − visual, aural and olfactory elements.
							</li>
							<li>
								<b>Process: </b>Systems and processes involved in delivering a consistent service. Different types of processes used to expedite the marketing function.
							</li>
						</ul>
						<p>
							<b>Achieving overall business objectives:</b>
							The shift from the 4Ps to the 7Ps and the significance of the extended marketing mix.
							An overview of the marketing planning process (Analysis, Planning, Implementation and Control) and marketing strategy.
						</p>

						<hr class="divider-color" />
