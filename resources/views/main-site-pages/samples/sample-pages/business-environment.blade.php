                        <p class="fs-18"><strong>Task 3 : 3.1 Explain how pricing and output decisions of businesses are determined in the following market structures

                        </strong></p>
                        <p>
                        	The population of buyers and sellers in a market forms the market structures. 

                        </p>
                        <strong>
                        	Perfect Competition
                        </strong>
                        <p>
                        	In a market where there exist different investors with same products and services, the price of the product or service and the output decision will depend on the demand for these goods. Competitive markets see the buyers determining the price and company engages in the production decision comparing how demanding the product is in the market and every firm makes attempts to lower the costs to the customer. The main reason behind these moves is to make a significant increase in the market share (Baumol & Blinder, 2009). 

                        </p>
                        <strong>
                        	Monopoly
                        </strong>
                        <p>
                        	Monopoly is a market design where an organization might be having exclusive control over the market, and it deemed to be the only provider. In such cases, the prices of goods and services in the market are determined by the company, and it also has the rule over the output decision as it will be in a monopolistic market. The organization will be in a position to charge the prices they feel, and the customers will have to pay as they will have no alternatives. These firms produce products and offer services with full capacity as they will be bound to fulfil the demands as a whole for their products. Most of the businesses operating in such structures are the international organizations. (Reference) 

                        </p>
                        <strong>
                        	Oligopoly
                        </strong>
                        <p>
                        	Here we have a market structure with many large sellers of the same commodity, and they all have the control over the prices as they all set an on-going rate for their products in the market. All the companies charge the same prices for their products and the customer as an option of choosing whom to buy from. These businesses can alter the same prices, but they will differ in their manner of advertisement to attract their loyal customers. In this structure, the companies take the decisions of output depending on the demand from their clients for their goods. (Reference)

                        </p>
                        <strong>
                        	Duopoly
                        </strong>
                        <p>
                        	Duopoly is a market structure where two enterprises or countries are in control of all or close to all the market for their products and services. The two companies will compete on the prices, and they will always adjust their prices to attract more customers. The price of their products is dependent on the quality of their goods, and these structures always end up with a zero-profits price. The output decision is made by the companies as they decide how much to produce compared to their rivals (Dabbah, 2004). 

                        </p>

