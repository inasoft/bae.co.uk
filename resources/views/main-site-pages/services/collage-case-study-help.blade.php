						<p>The students can create their case studies on both the conflicts of the discipline and in the details. By setting up an own <strong>case study, </strong> the student can also improve their creative thoughts and can gain some new insights. As a student, you can choose your case study depending on the basic rules of a good case and <strong>structure of the case study</strong>. The teachers of the college can suggest the students write a case study on few basic situations, issues and concepts. If the teacher tells the students how many characters do the students need to involve in the case study, it will be very helpful to the students. Your teacher can also advise you about the structure of the case study. The students can analyse the case studies individually or in a group. The may divide the students of a class into some groups for <strong>case study analysis</strong> and give different case studies to each group. On a keyboard, only one pair of hand can be fitted up, so it will be problematic to do the case studies without paper outside the class. If a student involves a case study, it can be a long-term activity. </p>
<p>Before writing the case study, the students have to identify the problem of the research. The class of the student or the college of the students does not matter so much. When a case study is given to a student in college, before starting the case study, the student has to discuss with the teachers about the topic of the case study. It will be challenging for the student if he does not follow the <strong>instruction of the case study. </strong> After reading the instruction, the student will start to read the problem of the case study and how can it be solved. At the time of doing a case study, you can gather the information from many places. The information of the case study can be gathered from online books, textbooks, newspapers and history books. You have to specify which information will more suitable for your case study, or you have to shorten your information After gathering all the information. During reducing the information, you have to focus on the topic of the case study. </p>
<p>There are some rules to writing a good case study; those are as follows.
<ul><li>In the past five yearsA good case is set </li>
<li>A high-quality case study  has generality</li>
