<p>As a college student, you must follow a good <strong>framework of case study</strong> writing. The steps of a <strong>high-quality case study framework</strong> are as follows. </p>
<p>Title page: you must put a good and identifying title of tour case study. A contented title reflects the entire story of a case study. After reading the title, your teacher can easily understand the problem of the case study. </p>
<p>Abstract: You must write an abstract of your case study after the title. The abstract will describe the entire case study in a small story. The abstract will reveals the process of doing the case study, the problem of the case study and the solution of the problem of the case study. </p> 
<p>Introduction: In this section, you will introduce the topic of the case study to the reader. You can also prescribe the sources from where you have gathered the information. </p>
<p>Presentation of fact: In the presentation section of the case study, you will present the problem of the case study. You can state the issues that are potentially harmful, and you can also state those issues that you have to tackle during the case study.  </p>
<p>Discussion: The discussion section reveals the own mindset of the college student. In the section of the case study, you will interpret why the problem has arrived. The discussion draws a more elaborates pictures of the case study. </p>
<p>Solution: You have to state the solution to the problem of the case study in this section. In this step of the <strong>case study, </strong> you should make sure that the solution must be specific.  </p>
<p>References: You would refer the sources from where you have gathered the information in this section. </p>

