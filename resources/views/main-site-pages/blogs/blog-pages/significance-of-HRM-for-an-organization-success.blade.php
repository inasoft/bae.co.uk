						<h3>Significance of HRM for an Organization Success</h3>
						<p>With a rapidly changing business environment and trends, today companies pay a lot of attention to their staff. In addition to this, with a steady movement towards service based economy, it has also become important for service sector firms to keep their people encouraged as well as productive. The changing trends in turn have directed the companies towards a paradigm shift in way they view their staff. Currently, firms view their employees as more than just resources along with the use of people first approach. This overall shift in firms thinking towards employees have directed towards an evolution of HRM as a crucial function of organizational success.</p>

