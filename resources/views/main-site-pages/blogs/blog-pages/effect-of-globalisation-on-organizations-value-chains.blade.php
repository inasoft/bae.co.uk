						<h3>Effect of Globalization on Organizations Value Chains</h3>
						<p>With the origination of globalization, businesses have witnesses a radical change in the way they operate, manage raw materials and relations with their suppliers. Though, it seems to be an effective opportunity, but it also demands operating with a global value chain (primary and support activities). Managing it has its own pros and cons, along with the complexities involved. In past, the set of these activities undertaken by firms were useful to develop value for their customers, but now most of these activities are outsourced. This in turn makes a firm more dependent nowadays as it has less control over different primary and support activities.</p>

						<p>
							If a companyâ€™s operations are in its own control, then it becomes easy for the management to have effective access to all information, and take decisions on time. Outsourcing on the other hand has directed the businesses towards less information available with them, at different levels of production. In this regard, it becomes vital to manage their different worldwide primary and support activities. Regarding this, firms significantly need to manage some of its most important primary and support activities on their own rather than outsourcing it.  Lesser outsourcing will make it easy for the management to track, trace, and monitor their levels of production, as well as decrease costs.  
						</p>

						<p>
							On the basis of this, it can be said that while operating a global value chain it is important for a firm to consider different business aspects such as competitive advantage, financial position, technological developments, human resources, cultural aspects, political and economic environment, corporate social responsibility etc. With the consideration of all these business aspects, it will become easy for a business to manage its value chain activities as per their internal as well as external condition. These considerations will facilitate firms to identify how they can effectively operate at global level by making right decision of managing critical activities on their own and outsourcing related business activities. 
